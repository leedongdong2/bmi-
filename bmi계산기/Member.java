package BMI;

 class Member {
 
	 private String id;
	 private String password;
	 private String age;
	 private String sex;
	 private String email;
	 
	String getId() {
		return id;
	}
	void setId(String id) {
		this.id = id;
	}
	String getPassword() {
		return password;
	}
	void setPassword(String password) {
		this.password = password;
	}
	String getAge() {
		return age;
	}
	void setAge(String age) {
		this.age = age;
	}
	String getSex() {
		return sex;
	}
	void setSex(String sex) {
		this.sex = sex;
	}
	String getEmail() {
		return email;
	}
	void setEmail(String email) {
		this.email = email;
	}
	 
	
	
}
